package sn.dgcpt.missionsparc.mission;

public class CreationMissionForm {
    private Integer posteId;
    private String codePoste, nomPoste;
    private String objet, dateDebut, dateFin;
    private String chefMissionSel, chefMissionMat, chefMissionNom, chefMissionPrenom;
    private String chefPosteSel, chefPosteMat, chefPosteNom, chefPostePrenom;

    public Integer getPosteId() { return posteId; }
    public void setPosteId(Integer posteId) { this.posteId = posteId; }
    public String getCodePoste() { return codePoste; }
    public void setCodePoste(String codePoste) { this.codePoste = codePoste; }
    public String getNomPoste() { return nomPoste; }
    public void setNomPoste(String nomPoste) { this.nomPoste = nomPoste; }
    public String getObjet() { return objet; }
    public void setObjet(String objet) { this.objet = objet; }
    public String getDateDebut() { return dateDebut; }
    public void setDateDebut(String dateDebut) { this.dateDebut = dateDebut; }
    public String getDateFin() { return dateFin; }
    public void setDateFin(String dateFin) { this.dateFin = dateFin; }
    public String getChefMissionSel() { return chefMissionSel; }
    public void setChefMissionSel(String v) { this.chefMissionSel = v; }
    public String getChefMissionMat() { return chefMissionMat; }
    public void setChefMissionMat(String v) { this.chefMissionMat = v; }
    public String getChefMissionNom() { return chefMissionNom; }
    public void setChefMissionNom(String v) { this.chefMissionNom = v; }
    public String getChefMissionPrenom() { return chefMissionPrenom; }
    public void setChefMissionPrenom(String v) { this.chefMissionPrenom = v; }
    public String getChefPosteSel() { return chefPosteSel; }
    public void setChefPosteSel(String v) { this.chefPosteSel = v; }
    public String getChefPosteMat() { return chefPosteMat; }
    public void setChefPosteMat(String v) { this.chefPosteMat = v; }
    public String getChefPosteNom() { return chefPosteNom; }
    public void setChefPosteNom(String v) { this.chefPosteNom = v; }
    public String getChefPostePrenom() { return chefPostePrenom; }
    public void setChefPostePrenom(String v) { this.chefPostePrenom = v; }
}
